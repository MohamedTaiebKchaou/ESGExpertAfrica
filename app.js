const langToggle = document.getElementById("langToggle");
const menuToggle = document.getElementById("menuToggle");
const siteNav = document.getElementById("siteNav");
const yearEl = document.getElementById("year");
const htmlRoot = document.documentElement;

function applyTranslations(lang) {
  const dict = translations[lang];
  if (!dict) return;

  htmlRoot.setAttribute("lang", lang === "fr" ? "fr" : "en");

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  langToggle.textContent = lang === "fr" ? "EN" : "FR";
  localStorage.setItem("esgexpert_lang", lang);
}

function initLanguage() {
  const saved = localStorage.getItem("esgexpert_lang");
  const browserLang = navigator.language && navigator.language.startsWith("fr") ? "fr" : "en";
  const initialLang = saved || browserLang || "fr";
  applyTranslations(initialLang);
}

langToggle.addEventListener("click", () => {
  const current = localStorage.getItem("esgexpert_lang") || "fr";
  const next = current === "fr" ? "en" : "fr";
  applyTranslations(next);
});

menuToggle.addEventListener("click", () => {
  siteNav.classList.toggle("open");
});

document.querySelectorAll("#siteNav a").forEach((link) => {
  link.addEventListener("click", () => {
    siteNav.classList.remove("open");
  });
});

yearEl.textContent = new Date().getFullYear().toString();
initLanguage();
